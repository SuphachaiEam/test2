package th.priisoft.crm.common.model;

public class ResponseObject {
	private String resule_code;
	private String result_message;
	private String result_data;
	public String getResule_code() {
		return resule_code;
	}
	public void setResule_code(String resule_code) {
		this.resule_code = resule_code;
	}
	public String getResult_message() {
		return result_message;
	}
	public void setResult_message(String result_message) {
		this.result_message = result_message;
	}
	public String getResult_data() {
		return result_data;
	}
	public void setResult_data(String result_data) {
		this.result_data = result_data;
	}
	
}
